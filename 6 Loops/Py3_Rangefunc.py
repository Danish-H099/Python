# Range function in python is used to generate a range of function

for i in range(22):
    print(i) # op-> 0-21

for i in range(4,22):
    print(i) # op-> 4-21

#start,stop,step size -> is the parameters of range function
for i in range(4,22,2): # op-> 4-20 all even numbers
    print(i) # op-> 0-21